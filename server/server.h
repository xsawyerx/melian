#pragma once

// A Server embodies the Melian server.

#include "server_io.h"

struct conn_state_t;

// A running server.
typedef struct Server {
  // I/O backend selection
  IoBackend io_backend;
  void* io_ctx;                  // Backend-specific context (uring_ctx_t* for io_uring)

  // libevent backend (used when io_backend == IO_BACKEND_LIBEVENT)
  struct event_base *base;
  struct evconnlistener *listener;
  struct event *tev;
  struct event *sev;

  // Common components
  struct Config* config;
  struct Status* status;
  struct Data* data;
  struct DB* db;
  struct Cron* cron;
  struct conn_state_t* conn_free;
  unsigned running;
} Server;

Server* server_build(void);
void server_destroy(Server* server);

unsigned server_initial_load(Server* server);
unsigned server_listen(Server* server);
unsigned server_run(Server* server);
unsigned server_stop(Server* server);
